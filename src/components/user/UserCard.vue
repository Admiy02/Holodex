<template>
    <!-- Menu Card -->
    <!--
*  General goal: show User / prompt login.
*  
*  If logged in: (in no particular order)
*     show user name, contributions.
*     # of favorited channels
*     log out button
*     settings page link
*     
*  If not logged in:
*     social login buttons
*     settings page link
*     
                     *  
                     -->
    <v-card flat v-intersect="tryUpdateUser">
        <v-list>
            <v-list-item v-if="user">
                <v-list-item-avatar>
                    <img
                        :src="`https://avatars.dicebear.com/api/jdenticon/${user.id}.svg`"
                        alt="Avatar generated by your user ID"
                    />
                </v-list-item-avatar>
                <v-list-item-content>
                    <v-list-item-title>{{ user.username }}</v-list-item-title>
                    <v-list-item-subtitle>
                        <v-icon small :color="user.discord_id ? 'green lighten-2' : 'grey'">{{
                            icons.mdiDiscord
                        }}</v-icon>
                        &nbsp;
                        <v-icon small :color="user.google_id ? 'green lighten-2' : 'grey'">{{
                            icons.mdiGoogle
                        }}</v-icon>
                        &nbsp;
                        <v-icon small :color="user.twitter_id ? 'green lighten-2' : 'grey'">{{
                            icons.mdiTwitter
                        }}</v-icon>
                        &nbsp;
                    </v-list-item-subtitle>
                    <v-list-item-content class="primary--text d-inline">
                        <v-icon x-small class="d-inline">{{ icons.mdiStarFourPointsOutline }}</v-icon>
                        {{ user.contribution_count }} pts
                    </v-list-item-content>
                </v-list-item-content>
            </v-list-item>
            <v-list-item-group>
                <v-list-item v-if="!user" to="/login">
                    <v-list-item-action>
                        <v-icon>{{ icons.mdiLoginVariant }}</v-icon>
                    </v-list-item-action>
                    <v-list-item-content>{{ $t("component.mainNav.login") }}</v-list-item-content>
                </v-list-item>

                <v-divider></v-divider>
                <v-list-item to="/login">
                    <v-list-item-action>
                        <v-icon>{{ icons.mdiAccountCircleOutline }}</v-icon>
                    </v-list-item-action>
                    <v-list-item-content>{{ $t("component.mainNav.accountSettings") }}</v-list-item-content>
                </v-list-item>

                <v-list-item to="/settings">
                    <v-list-item-action>
                        <v-icon>{{ icons.mdiCog }}</v-icon>
                    </v-list-item-action>
                    <v-list-item-content>{{ $t("component.mainNav.settings") }}</v-list-item-content>
                </v-list-item>
                <v-list-item v-if="user" @click.prevent="logout">
                    <v-list-item-action>
                        <v-icon>{{ icons.mdiLogoutVariant }}</v-icon>
                    </v-list-item-action>
                    <v-list-item-content>{{ $t("component.mainNav.logout") }}</v-list-item-content>
                </v-list-item>
            </v-list-item-group>
        </v-list>
    </v-card>
</template>

<script>
import * as icons from "@/utils/icons";
import backendApi from "@/utils/backend-api";

export default {
    name: "UserCard",
    data: () => ({
        icons,
    }),
    computed: {
        userdata() {
            return this.$store.state.userdata;
        },
        user() {
            return this.$store.state.userdata.user;
        },
    },
    methods: {
        hashCode(x) {
            let hash = 0;
            // eslint-disable-next-line no-plusplus
            for (let i = 0; i < x.length; i++) {
                const character = x.charCodeAt(i);
                // eslint-disable-next-line no-bitwise
                hash = (hash << 5) - hash + character;
                // eslint-disable-next-line no-bitwise
                hash &= hash; // Convert to 32bit integer
            }
            return hash;
        },
        logout() {
            this.$store.dispatch("logout");
        },
        async tryUpdateUser() {
            if (this.userdata && this.userdata.jwt && Date.now() - (window.lastUserCheck || 0) > 60000) {
                window.lastUserCheck = Date.now();
                this.forceUserUpdate();
            }
        },
        async forceUserUpdate() {
            const check = await backendApi.loginIsValid(this.userdata.jwt);
            if (check.data && check.data.id) {
                this.$store.commit("setUser", { user: check.data, jwt: this.userdata.jwt });
            } else {
                this.$store.dispatch("logout");
            }
        },
    },
};
</script>

